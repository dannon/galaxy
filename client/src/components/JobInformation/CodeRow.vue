<template>
    <tr
        v-b-tooltip.hover
        :title="`click to ${action}`"
        @mousedown="mouseIsDown = true"
        @mousemove="mouseIsDown ? (mouseMoved = true) : (mouseMoved = false)"
        @mouseup="toggleExpanded()">
        <td>
            {{ codeLabel }}
        </td>
        <td v-if="codeItem">
            <GRow align-v="center">
                <GCol cols="11">
                    <pre :class="codeClass">{{ codeItem }}</pre>
                </GCol>
                <GCol class="nopadding pointer">
                    <font-awesome-icon :icon="iconClass" />
                </GCol>
            </GRow>
        </td>
        <td v-else><i>empty</i></td>
    </tr>
</template>
<script>
import { library } from "@fortawesome/fontawesome-svg-core";
import { faCompressAlt, faExpandAlt } from "@fortawesome/free-solid-svg-icons";
import { FontAwesomeIcon } from "@fortawesome/vue-fontawesome";

import GCol from "@/component-library/GCol.vue";
import GRow from "@/component-library/GRow.vue";

library.add(faCompressAlt, faExpandAlt);
export default {
    components: {
        GRow,
        GCol,
        FontAwesomeIcon,
    },
    props: {
        codeLabel: String,
        codeItem: String,
    },
    data() {
        return {
            mouseIsDown: false,
            mouseMoved: false,
            expanded: false,
        };
    },
    computed: {
        action() {
            return this.expanded ? "collapse" : "expand";
        },
        codeClass() {
            return this.expanded ? "code" : "code preview";
        },
        iconClass() {
            return this.expanded ? ["fas", "compress-alt"] : ["fas", "expand-alt"];
        },
    },
    methods: {
        toggleExpanded() {
            this.mouseIsDown = false;
            if (this.codeItem && !this.mouseMoved) {
                this.expanded = !this.expanded;
            }
        },
    },
};
</script>

<style scoped>
.pointer {
    cursor: pointer;
}
.nopadding {
    padding: 0;
    margin: 0;
}
</style>
